#!/bin/sh
verbose='false'

while getopts "vh" OPTION
do
  case $OPTION in
	  v)
	  verbose='true'
	  ;;
		h)
    echo 'usage: '$0' [-v]'
		exit 0
		;;
		*)
		# unrecognized args, quit
		exit 0
		;;
  esac
done
# Reset $@
#shift `echo $OPTIND-1 | bc`

echo '// this file was generated by '$0 $@
echo 'load("consolerunner.js");'
for file in `find TestCases -name '*.js'`
do
  echo 'ES5Harness.loadTest("'$file'");'
done
echo 'ES5Harness.startTesting('$verbose');'